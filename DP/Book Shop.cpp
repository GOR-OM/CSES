int main()
{
      int n, x;
    cin >> n >> x;
    vector<int> price(n);
    vector<int> pages(n);
    for (int i = 0; i < n; i++) {
        cin >> price[i];
    }
    for (int i = 0; i < n; i++) {
        cin >> pages[i];
    }

    vector<vector<int>> dp(n + 1, vector<int>(x + 1, 0));

    for (int i = 1; i <= n; i++) {
        for (int j = 0; j <= x; j++) {
            // If we don't take the current book
            dp[i][j] = dp[i-1][j];
            // If we take the current book and it fits in the current price limit
            if (j >= price[i-1]) {
                dp[i][j] = max(dp[i][j], dp[i-1][j - price[i-1]] + pages[i-1]);
            }
        }
    }

    cout << dp[n][x] << endl;
}