int solve(string s,vector<int>&dp){
    int n = stoi(s);
    if(n==0)return 0 ;
    if(dp[n]!=-1)return dp[n];

    int ans=1e9;
    for(int i=0;i<s.length();i++){
        int digit = s[i] - '0';
        if(s[i]-'0'!=0){
            ans = min(ans, solve(to_string(n - digit), dp) + 1);
        }
    }
    return dp[n]= ans;

    

}








